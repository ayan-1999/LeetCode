class Solution{
  public:
    
    // A[]: input array
    // N: input array
    //Function to sort the binary array.
    void binSort(int a[], int n)
    {
       int l=0;
       int r=n-1;
       while(l<r)
       {
            if(a[l]>a[r])
            {
                swap(a[l],a[r]);
            }
            if(a[l]==0)
                l++;
            if(a[r]==1)
                r--;
       }
        
       
    }
};

